class Operator 
{
	public static void main(String[] args) 
	{
		int num1 = 1;
		int num2 = 2;
		int num3 = 3;

		System.out.println(num1 > num2);
		System.out.println(num1 < num2);
		System.out.println(num1 >= num2);
		System.out.println(num1 <= num2);
		System.out.println(num1 == num2);
		System.out.println(num1 % num2);
		System.out.println(num1 + num2 % num3);

		
	}
}
